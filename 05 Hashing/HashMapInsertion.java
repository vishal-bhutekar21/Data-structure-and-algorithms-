import java.util.HashMap;

public class HashMapInsertion {
    public static void main(String[] args) {
        // Create a HashMap
        HashMap<Integer, String> map = new HashMap<>();

        // Insert elements into the HashMap
        map.put(1, "Apple");
        map.put(2, "Banana");
        map.put(3, "Orange");

        // Print the HashMap
        System.out.println("HashMap after insertion: " + map);

        
    }
}
