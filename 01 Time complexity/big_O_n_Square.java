public class big_O_n_Square {

    public static void numbersPrint(int n) {
        for(int i = 0; i<n; i++) {
            for(int j = 0; j < n; j++) {
           System.out.println(i+" "+j);
        }
    }

        
    
         
    }
    public static void main(String[] args) {
        numbersPrint(32);
        
    }
}